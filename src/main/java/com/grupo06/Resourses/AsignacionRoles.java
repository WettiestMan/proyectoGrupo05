package com.grupo06.Resourses;

/**
 *
 * @author santi
 */
public class AsignacionRoles implements java.io.Serializable{
    private int idRol;
    private int idEmpleado;

    public AsignacionRoles(int idEmpleado, int idRol) {
        this.idRol = idRol;
        this.idEmpleado = idEmpleado;
    }

    public int getIdRol() {
        return idRol;
    }

    public void setIdRol(int idRol) {
        this.idRol = idRol;
    }

    public int getIdEmpleado() {
        return idEmpleado;
    }

    public void setIdEmpleado(int idEmpleado) {
        this.idEmpleado = idEmpleado;
    }
    
}
